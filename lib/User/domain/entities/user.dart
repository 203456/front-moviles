class User {
  final int id;
  final String name;
  final String email;
  final String username;
  final String password;
  final String location;
  final String birth;
  final String gamertag;

  User(
      {required this.id,
      required this.name,
      required this.email,
      required this.username,
      required this.password,
      required this.location,
      required this.birth,
      required this.gamertag});
}
